import {
  CREATE_CLASS,
  CLEAR_MESSAGE,
} from './constants';

/**
 * Changes the input field of the form
 *
 * @param  {name} name The new text of the input field
 *
 * @return {object}    An action object with a type of CREATE_CLASS.REQUEST
 */
export function createClass(data) {
  return {
    type: CREATE_CLASS.REQUEST,
    data,
  };
}

/**
 * Changes the input field of the form
 *
 * @param  {name} name The new text of the input field
 *
 * @return {object}    An action object with a type of CREATE_CLASS.SUCCESS
 */
export function createClassSuccess() {
  return {
    type: CREATE_CLASS.SUCCESS,
  };
}

/**
 * Changes the input field of the form
 *
 * @param  {name} name The new text of the input field
 *
 * @return {object}    An action object with a type of CREATE_CLASS.FAILURE
 */
export function createClassFailure(error) {
  return {
    type: CREATE_CLASS.FAILURE,
    error,
  };
}

/**
 * Changes the input field of the form
 *
 * @param  {name} name The new text of the input field
 *
 * @return {object}    An action object with a type of CREATE_CATEGORY.SUCCESS
 */
export function clearMessage() {
  return {
    type: CLEAR_MESSAGE,
  };
}
